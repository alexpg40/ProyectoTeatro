    private void btnRegistrarseMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnRegistrarseMouseClicked
        
        String Nombre = null, Apellido = null, NIF = null, Telefono = null, Email = null, Password = null;
        
        if (campoNombre.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "El campo Nombre est√° sin rellenar." ,"Error: Introduzca los datos necesarios.",WARNING_MESSAGE);
            evt.consume();
        } else if (campoNombre.getText().length() > 45){
            JOptionPane.showMessageDialog(this, "El campo Nombre es mayor de 45 car√°cteres." ,"Error: El Nombre es muy largo.",WARNING_MESSAGE);
            evt.consume();
        } else if (campoNombre.getText().matches(".*\\d.*")) {
            JOptionPane.showMessageDialog(this, "El campo Nombre no puede tener numeros." ,"Error: El nombre tiene letras.",WARNING_MESSAGE);
            evt.consume();
        } else {
            Nombre = campoNombre.getText();
        }
        
        if (campoApellido.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "El campo Apellido est√° sin rellenar." ,"Error: Introduzca los datos necesarios.",WARNING_MESSAGE);
            evt.consume();
        } else if (campoApellido.getText().length() > 45) {
            JOptionPane.showMessageDialog(this, "El campo Apellido es mayor de 45 car√°cteres." ,"Error: El apellido es muy largo.",WARNING_MESSAGE);
            evt.consume();
        } else if (campoApellido.getText().matches(".*\\d.*")) {
            JOptionPane.showMessageDialog(this, "El campo Apellido no puede tener numeros." ,"Error: El apellido tiene numeros.",WARNING_MESSAGE);
            evt.consume();
        } else {
            Apellido = campoApellido.getText();
        }
        
        if (campoNif.getText().length() != 9) {
            JOptionPane.showMessageDialog(this, "El campo NIF tiene que tener una longitud de 9 car√°cteres." ,"Error: El NIF no tiene una longitud adecuada..",WARNING_MESSAGE);
            evt.consume();
        } else if (!Utilidades.isNumeric(campoNif.getText().substring(0, 8))) {
            JOptionPane.showMessageDialog(this, "Solo el ultimo d√≠gito del NIF puede ser una letra." ,"Error: Solo ultimo digito puede ser letra",WARNING_MESSAGE);
            evt.consume();
        } else if (!String.valueOf(campoNif.getText().charAt(8)).matches("[a-zA-Z]")) {
            JOptionPane.showMessageDialog(this, "El ultimo d√≠gito del NIF debe de ser una letra." ,"Error: El ultimo digito debe ser una letra",WARNING_MESSAGE);
            evt.consume();
        } else if (UsuarioDAO.comprobarNIF(campoNif.getText()) == true) {
            JOptionPane.showMessageDialog(this, "El NIF ya est√° en uso." ,"Error: NIF repetido.",WARNING_MESSAGE);
            evt.consume();
        }else {
            NIF = campoNif.getText();
        }
        
        if (campoTelefono.getText().length() != 9) {
            JOptionPane.showMessageDialog(this, "El campo Telefono tiene que tener una longitud de 9 car√°cteres." ,"Error: El NIF no tiene una longitud adecuada..",WARNING_MESSAGE);
            evt.consume();
        } else if (!Utilidades.isNumeric(campoTelefono.getText().substring(0,8))){
            JOptionPane.showMessageDialog(this, "El campo Telefono tiene que ser solo numeros." ,"Error: El Telefono tiene letras.",WARNING_MESSAGE);
            evt.consume();
        } else {
            Telefono = campoTelefono.getText();
        }
        
        Pattern patron = Pattern.compile("([a-z0-9]+(\\.?[a-z0-9])*)+@(([a-z]+)\\.([a-z]+))+");
        Matcher correo = patron.matcher(campoEmail.getText());
        
        if (campoEmail.getText().isEmpty()) {
          JOptionPane.showMessageDialog(this, "El campo Email no est√° introducido" ,"Error: El Email est√° vac√≠o.",WARNING_MESSAGE);
          evt.consume();
        } else if (UsuarioDAO.comprobarCorreo(campoEmail.getText()) == true) {
            JOptionPane.showMessageDialog(this, "El email ya existe." ,"Error: El Email ya existe.",WARNING_MESSAGE);
            evt.consume();
        } else if (correo.find() == true){
            Email = campoEmail.getText();
        } else {
            evt.consume();
        }
        
        if (campoContrasena.getPassword().toString().isEmpty() || campoContrasena.getText().length() < 5) {
            JOptionPane.showMessageDialog(this, "El campo Contrase√±a tiene que tener una longitud de al menos 5 car√°cteres." ,"Error: La contrase√±a no tiene una longitud adecuada..",WARNING_MESSAGE);
            evt.consume();
        } else {
           Password = campoContrasena.getText();
        }
        if (Nombre != null && Apellido != null && NIF != null && Telefono != null && Email != null && Password != null) {
            if (JOptionPane.showConfirmDialog(this, "Nombre introducido: " + Nombre + "\n" + "Apellido introducido: " + Apellido + "\n" + "NIF introducido: " + NIF + "\n" + "Telefono introducido: " + Telefono + "\n" + "Email introducido: " + Email) == 0) {
                Usuario u = new Usuario();
                u.setNombre(Nombre);
                u.setApellidos(Apellido);
                u.setNif(NIF);
                u.setTelefono(Telefono);
                u.setEmail(Email);
                u.setPassword(Password);
                UsuarioDAO.insertarUsuario(u);
                JOptionPane.showMessageDialog(this, "El registro se ha realizado con exito.", "Exito", INFORMATION_MESSAGE);

                this.setVisible(false);
            };
        }        
        

        
    }//GEN-LAST:event_btnRegistrarseMouseClicked

