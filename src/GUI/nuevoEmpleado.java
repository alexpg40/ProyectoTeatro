    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        long idEmpleado = Long.parseLong(this.jTextFieldID.getText());
        String nombre = this.jTextFieldNombre.getText();
        String apellido = this.jTextFieldApellido.getText();
        String NIF = this.jTextFieldNIF.getText();
        String direccion = this.jTextFieldDireccion.getText();
        String telefono = this.jTextFieldTelefono.getText();
        long idFranquicia = Long.parseLong(this.jTextFieldidFranquicia.getText());
        long idNomina = Long.parseLong(this.jTextFieldidNomina.getText());
        Entidades.Empleado e = new Entidades.Empleado(idEmpleado, nombre, apellido, NIF, direccion, telefono, idFranquicia, idNomina);
        int n = e.validarEmpleado();
        switch (n) {
            case 0:
                int create = JOptionPane.showConfirmDialog(this, "Estas seguro que quieres crear el empleado: " + "\n"
                        + "ID: " + String.valueOf(idEmpleado) + ", Nombre: " + nombre
                        + ", Apellido: " + apellido + ", NIF: " + NIF
                        + ", Direcci√≥n: " + direccion + ", Telefono: " + telefono
                        + ", idFranquicia: " + String.valueOf(idFranquicia) +  "y idNomina: " + String.valueOf(idNomina),
                         "Confirmar", JOptionPane.OK_CANCEL_OPTION);
                if (create == 0) {
                    if (this.jCheckBox1.isSelected()) {
                        EmpleadoDAO.insertarEmpleado(e);
                    } else {
                        EmpleadoDAO.insertarEmpleadoConID(e);
                    }
                }
                break;
            case 1:
                JOptionPane.showMessageDialog(this, "El ID no es v√°lido", "Error", JOptionPane.ERROR_MESSAGE);
                break;
            case 2:
                JOptionPane.showMessageDialog(this, "El nombre no es v√°lido", "Error", JOptionPane.ERROR_MESSAGE);
                break;
            case 3:
                JOptionPane.showMessageDialog(this, "El apellido no es v√°lido", "Error", JOptionPane.ERROR_MESSAGE);
                break;
            case 4:
                JOptionPane.showMessageDialog(this, "El NIF no es v√°lido", "Error", JOptionPane.ERROR_MESSAGE);
                break;
            case 5:
                JOptionPane.showMessageDialog(this, "El direccion no es v√°lido", "Error", JOptionPane.ERROR_MESSAGE);
                break;
            case 6:
                JOptionPane.showMessageDialog(this, "El telefono no es v√°lido", "Error", JOptionPane.ERROR_MESSAGE);
                break;
        }
    }//GEN-LAST:event_jButton1ActionPerformed

